package io.github.tofodroid.mods.mimi.util;

public class Vector2Float {
    public final Float x;
    public final Float y;

    public Vector2Float(Float x, Float y) {
        this.x = x;
        this.y = y;
    }

    public Float x() {
        return x;
    }

    public Float y() {
        return y;
    }
}
