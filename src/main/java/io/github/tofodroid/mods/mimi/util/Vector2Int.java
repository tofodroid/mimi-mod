package io.github.tofodroid.mods.mimi.util;

public class Vector2Int {
    public final Integer x;
    public final Integer y;

    public Vector2Int(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    public Integer x() {
        return x;
    }

    public Integer y() {
        return y;
    }
}
